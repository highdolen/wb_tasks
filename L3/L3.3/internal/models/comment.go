package models

import "time"

type Comment struct {
	ID        int64     `json:"id"`
	ParentID  *int64    `json:"parent_id,omitempty"`
	Text      string    `json:"text"`
	CreatedAt time.Time `json:"created_at"`

	Path  string `json:"-"`
	Level int    `json:"level"`

	Children []*Comment `json:"children,omitempty"`
}
